package com.example.ankit.mixlyrics;

class Search_resultsModel {

		public String director;
		public String lyrics;
		public String id;
		public String movie;
		public String song_url;
		public String lyricist;
		public String start_url;
		public String song;
		public String movie_url;
		public String singers;

public Search_resultsModel(String director, String lyrics, String id, String movie, String song_url, String lyricist, String start_url, String song, String movie_url, String singers) {

		this.director = director;
		this.lyrics = lyrics;
		this.id = id;
		this.movie = movie;
		this.song_url = song_url;
		this.lyricist = lyricist;
		this.start_url = start_url;
		this.song = song;
		this.movie_url = movie_url;
		this.singers = singers;

	}

}