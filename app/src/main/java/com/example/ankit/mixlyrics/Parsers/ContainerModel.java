import java.util.ArrayList;

class ContainerModel {

	public String desc;
	public String updateded;
	public String post;
	public ArrayList<String> tags;
	public String uploadedon;
	public String day;
	public String updatedon;
	public String udecs;
	public String remedy;
	public String aperson;

	public ContainerModel(String desc, String updateded, String post, ArrayList<String> tags, String uploadedon, String day, String updatedon, String udecs, String remedy, String aperson) {

		this.desc = desc;
		this.updateded = updateded;
		this.post = post;
		this.tags = tags;
		this.uploadedon = uploadedon;
		this.day = day;
		this.updatedon = updatedon;
		this.udecs = udecs;
		this.remedy = remedy;
		this.aperson = aperson;

	}

}