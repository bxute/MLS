
class StatuModel {

	public int duration;
	public String comment;
	public String list;
	public String reason;
	public String value;

	public StatuModel(int duration, String comment, String list, String reason, String value) {

		this.duration = duration;
		this.comment = comment;
		this.list = list;
		this.reason = reason;
		this.value = value;

	}

}