
class CopyrightModel {

	public String holder;
	public int year;
	public String holder_url;
	public String licence_url;
	public String logo;
	public String licence;

	public CopyrightModel(String holder, int year, String holder_url, String licence_url, String logo, String licence) {

		this.holder = holder;
		this.year = year;
		this.holder_url = holder_url;
		this.licence_url = licence_url;
		this.logo = logo;
		this.licence = licence;

	}

}